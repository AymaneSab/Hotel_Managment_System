package Classes;

public class Service {

    private int id;
    private String nom;
    private String responsable;
    private int nombreEmploye;
    private int nombrePoste;

    public Service() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public String getResponsable() {
        return responsable;
    }

    public void setResponsable(String responsable) {
        this.responsable = responsable;
    }

    public int getNombreEmploye() {
        return nombreEmploye;
    }

    public void setNombreEmploye(int nombreEmploye) {
        this.nombreEmploye = nombreEmploye;
    }

    public int getNombrePoste() {
        return nombrePoste;
    }

    public void setNombrePoste(int nombrePoste) {
        this.nombrePoste = nombrePoste;
    }
    
    
    
    
    
    
}
